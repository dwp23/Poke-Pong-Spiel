package Model;

public class XYTupel {

   float x = 0;

    float y = 0;

    XYTupel() {}

    public XYTupel(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX(){
        return  x;
    }

    public void setX(float x){
        this.x = x;

    }

    public void setY(float y){
        this.y = y;
    }

    public float getY(){

        return y;
    }









}
